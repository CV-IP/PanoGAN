if __name__ == '__main__':
    opt = TrainOptions().parse()  # get training options
    dataset = create_dataset(opt)  # create a dataset given opt.dataset_mode and other options
    dataset_size = len(dataset)  # get the number of images in the dataset.
    print('The number of training images = %d' % dataset_size)

    model = create_model(opt)  # create a model given opt.model and other options
    opt.visualizer = Visualizer(opt)  # create a visualizer that display/save images and plots
    opt.total_iters = 0  # the total number of training iterations

    if opt.training_scheme == 'main' or opt.training_scheme == 'all':
        print('------------ training the basic network ------------------')
        opt.continue_train = False
        model.setup(opt)
        model.set_loop_count(0)
        train_main(model, dataset, opt)

    if opt.training_scheme == 'afl' or opt.training_scheme == 'all':
        print('------------ training the feedback module and discriminator ------------------')
        opt.continue_train = True
        opt.niter = opt.niter + opt.epoch_count
        model.setup(opt)
        model.set_loop_count(opt.loop_count)
        train_afl(model, dataset, opt)
